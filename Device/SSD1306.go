	var iSSD1306 *SSD1306 = nil

func ISSD1306() (*SSD1306) {
	if (iSSD1306 == nil) {
		iSSD1306 = &SSD1306{}

		iSSD1306.mosi, _	= S500.CreateGPIO(S500.PC, 25)
		iSSD1306.miso, _	= S500.CreateGPIO(S500.PC, 24)
		iSSD1306.sclk, _	= S500.CreateGPIO(S500.PC, 22)
		iSSD1306.cs, _		= S500.CreateGPIO(S500.PC, 23)
		iSSD1306.spi, _ = S500.CreateSPI(iSSD1306.mosi, iSSD1306.miso, iSSD1306.sclk, iSSD1306.cs, S500.SPI_0)
		iSSD1306.spi.SetDIV(0x3FF)
		iSSD1306.spi.Open()
		
		iSSD1306.dc, _	= S500.CreateGPIO(S500.PC, 6)
		iSSD1306.dc.SetFun(S500.FunOUT)
		iSSD1306.rst, _ = S500.CreateGPIO(S500.PC, 5)
		iSSD1306.rst.SetFun(S500.FunOUT)

		iSSD1306.ssd1306_init()
	}

	return iSSD1306
}

func FreeSSD1306() {
	iSSD1306.spi.Close()
	S500.FreeSPI(iSSD1306.spi)
	S500.FreeGPIO(iSSD1306.dc)
	S500.FreeGPIO(iSSD1306.rst)
}

	type SSD1306 struct {
		spi		*S500.SPI
		mosi	*S500.GPIO
		miso	*S500.GPIO
		sclk	*S500.GPIO
		cs		*S500.GPIO
		dc		*S500.GPIO
		rst		*S500.GPIO
	}

func (this *SSD1306) ssd1306_cmd(cmd uint8) {
	this.dc.SetData(false)
	this.spi.RW8Bit([]uint8{cmd})
}

func (this *SSD1306) ssd1306_cmds(cmds []uint8, len uint32) {
	this.dc.SetData(false)
	this.spi.RW8Bit(cmds)
}

func (this *SSD1306) ssd1306_dat(dat uint8) {
	this.dc.SetData(true)
	this.spi.RW8Bit([]uint8{dat})
}

func (this *SSD1306) ssd1306_dats(dats []uint8, len uint32) {
	this.dc.SetData(true)
	this.spi.RW8Bit(dats)
}

func (this *SSD1306) ssd1306_init() {
	this.rst.SetData(false)
	this.rst.SetData(true)
	
	I := 0xFFFFFF
	for { I--; if I == 0 { break } }
	
	this.ssd1306_cmd(0xAE);//--turn off oled panel
	this.ssd1306_cmd(0x00);//---set low column address
	this.ssd1306_cmd(0x10);//---set high column address
	this.ssd1306_cmd(0x40);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
	this.ssd1306_cmd(0x81);//--set contrast control register
	this.ssd1306_cmd(0xCF); // Set SEG Output Current Brightness
	this.ssd1306_cmd(0xA1);//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
	this.ssd1306_cmd(0xC8);//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
	this.ssd1306_cmd(0xA6);//--set normal display
	this.ssd1306_cmd(0xA8);//--set multiplex ratio(1 to 64)
	this.ssd1306_cmd(0x3F);//--1/64 duty
	this.ssd1306_cmd(0xD3);//-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
	this.ssd1306_cmd(0x00);//-not offset
	this.ssd1306_cmd(0xD5);//--set display clock divide ratio/oscillator frequency
	this.ssd1306_cmd(0x80);//--set divide ratio, Set Clock as 100 Frames/Sec
	this.ssd1306_cmd(0xD9);//--set pre-charge period
	this.ssd1306_cmd(0xF1);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	this.ssd1306_cmd(0xDA);//--set com pins hardware configuration
	this.ssd1306_cmd(0x12);
	this.ssd1306_cmd(0xDB);//--set vcomh
	this.ssd1306_cmd(0x40);//Set VCOM Deselect Level
	this.ssd1306_cmd(0x20);//-Set Page Addressing Mode (0x00/0x01/0x02)
	this.ssd1306_cmd(0x02);//
	this.ssd1306_cmd(0x8D);//--set Charge Pump enable/disable
	this.ssd1306_cmd(0x14);//--set(0x10) disable
	this.ssd1306_cmd(0xA4);// Disable Entire Display On (0xa4/0xa5)
	this.ssd1306_cmd(0xA6);// Disable Inverse Display On (0xa6/a7)
	this.ssd1306_cmd(0xAF);//--turn on oled panel
	
	this.Clear(0x00)
	this.SetXY(0, 0)
}

func (this *SSD1306) SetXY(X uint8, Y uint8) {
	this.ssd1306_cmd(0xB0 + Y);
	this.ssd1306_cmd(((X & 0xF0) >> 4) | 0x10);
	this.ssd1306_cmd((X & 0x0F) | 0x00);
}

func (this *SSD1306) Clear(Data uint8) {
	var X, Y uint8
	
	Buf := make([]uint8, 128)
	for X = 0; X < 128; X++ { Buf[X] = Data }
	
	this.SetXY(0, 0)
	for Y = 0; Y < 8; Y++ {
		this.ssd1306_cmds([]uint8{0xB0 + Y, 0x01, 0x10}, 3)
		this.ssd1306_dats(Buf, 128)
	}
}

func (this *SSD1306) Open() {
	this.ssd1306_cmds([]uint8{0x8D, 0x14, 0xAF}, 3)
}

func (this *SSD1306) Close() {
	this.ssd1306_cmds([]uint8{0x8D, 0x10, 0xAE}, 3)
}

func (this *SSD1306) Write(X uint8, Y uint8, Chr uint8) {
	var I uint32 = 0
	
	C := uint32(Chr - 32) * 16;
	this.SetXY(X, Y)
	for I = 0; I < 8; I++ { this.ssd1306_dat(F8x16[C + I]) }
	this.SetXY(X, Y + 1)
	for I = 8; I < 16; I++ { this.ssd1306_dat(F8x16[C + I]) }
}

func (this *SSD1306) Writes(X uint8, Y uint8, Str []uint8) {
	for I := 0; I < len(Str); I++ { this.Write(X + uint8(I * 8), Y * 2, Str[I]) }
}

